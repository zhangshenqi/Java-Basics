/**
 * Interface for shape.
 * @author Shenqi Zhang
 *
 */
public interface Shape {
    /**
     * Gets the area of this shape.
     * @return area of this shape
     */
    double getArea();
    
    /**
     * Gets the circumference of this shape.
     * @return circumference of this shape
     */
    double getCircumference();
}
